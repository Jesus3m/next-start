import { FC, useState } from 'react'
import { Router } from 'next/dist/client/router'
import { Aside } from './aside/aside.component'
import { MainContent } from './layout.styled'
import { Nav } from './nav/nav.component'
import Head from 'next/head'

export const Layout: FC<{router: Router}> = ({ children, router }) => {
  const [sideWidth, setSideWidth] = useState(false) // estado que define el ancho del sidebar

  // Colapsar o desplegar la navegacion lateral
  const toggleAside = () => {
    setSideWidth(prev => !prev)
  }

  return (
        <>
            <Head>
                <title>{router.pathname.split('/')[1]}</title>
                <meta name='description' content='Generated by create next app' />
                <link rel='icon' href='/favicon.ico' />
            </Head>
            <Aside show={sideWidth} toggleAside={toggleAside}/>
            <Nav toggleAside={toggleAside} show={sideWidth}/>
            <MainContent show={sideWidth}>
                { children }
            </MainContent>
        </>
  )
}
